#include "libft.h"

size_t	ft_tablen(const char **tab)
{
	size_t	i;

	i = 0;
	if (tab == NULL)
		return (0);
	while (*tab != NULL)
	{
		tab = tab + 1;
		i = i + 1;
	}
	return (i);
}

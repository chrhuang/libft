#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == NULL || s2 == NULL)
		return (-1);
	while (*s1 != '\0' && *s2 != '\0' && n != 0)
	{
		if (*s1 != *s2)
			return (0);
		s1 = s1 + 1;
		s2 = s2 + 1;
		n = n - 1;
	}
	if (n == 0)
		return (1);
	return (*s1 == *s2 ? 1 : 0);
}
